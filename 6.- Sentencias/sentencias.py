# Sentencias en Python

## Sentencia if
edad = 20
if edad >= 18:
    print("Eres mayor de edad.")
else:
    print("Eres menor de edad.")

## Sentencia elif
nota = 85
if nota >= 90:
    print("Sobresaliente")
elif nota >= 75:
    print("Notable")
elif nota >= 60:
    print("Aprobado")
else:
    print("Suspenso")

## Operador ternario
es_mayor = "Sí" if edad >= 18 else "No"
print(f"¿Eres mayor de edad? {es_mayor}")

 



