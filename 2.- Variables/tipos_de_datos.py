#Tipos de Datos

# Ejemplos de tipos de datos en Python
cadena_texto = "Hola, Mundo!"  # Tipo str
numero_entero = 42             # Tipo int   
numero_flotante = 3.14         # Tipo float
valor_booleano = False         # Tipo bool

# Lista
lista_numeros = [1, 2, 3, 4, 5]  # Tipo list

# Tupla
tupla_colores = ("rojo", "verde", "azul")  # Tipo tuple

# Conjunto
conjunto_frutas = {"manzana", "banana", "cereza"}  # Tipo set

# Diccionario
diccionario_persona = {"nombre": "Ana", "edad": 25}  # Tipo dict

# Imprimir los tipos de datos
print("Tipo de cadena_texto:", type(cadena_texto))
print("Tipo de numero_entero:", type(numero_entero))
print("Tipo de numero_flotante:", type(numero_flotante))
print("Tipo de valor_booleano:", type(valor_booleano))
print("Tipo de lista_numeros:", type(lista_numeros))
print("Tipo de tupla_colores:", type(tupla_colores))
print("Tipo de conjunto_frutas:", type(conjunto_frutas))
print("Tipo de diccionario_persona:", type(diccionario_persona))


